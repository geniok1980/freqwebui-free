function translateText(input: string): string {
  const trimmed = input.trim();
  if (!trimmed) return input;
  if (exactMap[trimmed]) {
    return input.replace(trimmed, exactMap[trimmed]);
  }
  let out = input;
  for (const [en, ru] of Object.entries(tokenMap)) {
    const re = new RegExp(`\\b${en}\\b`, "g");
    out = out.replace(re, ru);
  }
  return out;
}

function translateNode(root: Node) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const textNodes: Text[] = [];
  let current = walker.nextNode();
  while (current) {
    textNodes.push(current as Text);
    current = walker.nextNode();
  }

  for (const node of textNodes) {
    const parent = node.parentElement;
    if (!parent) continue;
    const tag = parent.tagName.toLowerCase();
    if (tag === "script" || tag === "style") continue;
    const translated = translateText(node.nodeValue ?? "");
    if (translated !== node.nodeValue) {
      node.nodeValue = translated;
    }
  }

  if (root instanceof Element) {
    const elements = [root, ...Array.from(root.querySelectorAll("*"))];
    for (const el of elements) {
      if (el.hasAttribute("placeholder")) {
        const v = el.getAttribute("placeholder");
        if (v) el.setAttribute("placeholder", translateText(v));
      }
      if (el.hasAttribute("title")) {
        const v = el.getAttribute("title");
        if (v) el.setAttribute("title", translateText(v));
      }
      if ((el as HTMLInputElement).value && el.tagName.toLowerCase() === "input") {
        const input = el as HTMLInputElement;
        if (input.type === "button" || input.type === "submit") {
          input.value = translateText(input.value);
        }
      }
    }
  }
}

export function installRussianDomTranslator() {
  document.documentElement.lang = "ru";
  translateNode(document.body);

  const observer = new MutationObserver((mutations) => {
    for (const m of mutations) {
      if (m.type === "characterData") {
        if (m.target.nodeType === Node.TEXT_NODE) {
          const t = m.target as Text;
          const translated = translateText(t.nodeValue ?? "");
          if (translated !== t.nodeValue) t.nodeValue = translated;
        }
      } else {
        m.addedNodes.forEach((n) => translateNode(n));
      }
    }
  });

  observer.observe(document.body, {
    childList: true,
    subtree: true,
    characterData: true,
  });
}
